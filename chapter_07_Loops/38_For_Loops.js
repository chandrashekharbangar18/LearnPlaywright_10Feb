// For Loop --> helps us to repeat a block of code a certain number of times

// Syntax of For Loop
// for (initialization; condition; increment/decrement) {
//     // code to be executed
// }

// Example 1: Print numbers from 0 to 4
for (let i = 0; i < 5; i++) {
    console.log(i);
}

// Example 2: Print even numbers from 0 to 10
for (let j = 0; j <= 10; j += 2) {
    console.log(j);
}

