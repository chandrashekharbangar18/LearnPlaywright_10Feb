// Switch case Example
// This example demonstrates the use of switch case statements without break statements.
let day = 3;

switch(day) 
{
    case 1:
        console.log("Monday");   
    case 2:
        console.log("Tuesday");     
    case 3:
        console.log("Wednesday");
    case 4:
        console.log("Thursday");
    case 5:
        console.log("Friday");  
    case 6:
        console.log("Saturday");
    case 7:
        console.log("Sunday");
    default:
        console.log("Invalid day");
}