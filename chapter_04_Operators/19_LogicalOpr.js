// && --> AND
// || --> OR
// ! --> NOT

// Examples

let a=true;
let b=false;    
console.log(a && b); // false
console.log(a || b); // true
console.log(!a); // false
console.log(!b); // true    

let age=25;
console.log(age > 18 && age < 30); // true
console.log(age > 30 || age < 20); // false
console.log(!(age > 18)); // false  
