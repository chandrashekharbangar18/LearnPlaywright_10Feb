// Arithmetic Operators

let a=10;
let b=2;
let c=3;
console.log(a); // 10
console.log(b); // 2

let sum = a + b;
console.log(sum); // 12

let difference = a - b;
console.log(difference); // 8

let product = a * b;
console.log(product); // 20

let quotient = a / b;
console.log(quotient); // 5

let remainder = a % c;
console.log(remainder); // 1

console.log(a ** b); // 100 (10 raised to the power of 2)


// Assignment Operators
let x = 5;
x += 3; // x = x + 3;
console.log(x); // 8        
x -= 2; // x = x - 2;
console.log(x); // 6
x *= 4; // x = x * 4;
console.log(x); // 24
x /= 6; // x = x / 6;
console.log(x); // 4
x %= 3; // x = x % 3;
console.log(x); // 1
