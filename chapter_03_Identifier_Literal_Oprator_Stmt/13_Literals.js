// Literals
// Types of literals in JavaScript include like numeric literals, string literals, boolean literals,
//  object literals, array literals, null literals, and undefined literals.
let age = 25; // Numeric literal
let name = "Alice"; // string literal
let isStudent = true; // Boolean literal
let person = { name: "Bob", age: 30 }; // Object literal
let numbers = [1, 2, 3, 4, 5]; // Array literal
let nullValue = null; // Null literal
let undefinedValue; // Undefined literal (implicitly assigned)  
let zero = 0; // Numeric literal
let emptyString = "";   // String literal
let isActive = false; // Boolean literal
let car = { make: "Toyota", model: "Camry", year: 2020 }; // Object literal
let fruits = ["apple", "banana", "orange"]; // Array literal    
let emptyObject = {}; // Object literal
let emptyArray = []; // Array literal
let undefinedVar = undefined; // Undefined literal (explicitly assigned)
let pi = 3.14; // Numeric literal
let greeting1 = "Hello, World!"; // String literal
let isLoggedIn = false; // Boolean literal

// Template literals -->
// (also known as template strings) are a type of string literal that 
// allows for embedded expressions and multi-line strings. They are enclosed by backticks (`) 
// instead of single or double quotes.
let fname = "Alice";
let agee = 30;
let greeting = `Hello, my name is ${fname} and I am ${agee} years old.`;
console.log(greeting); // Output: Hello, my name is Alice and I am 30 years old.

// Template literals can also span multiple lines without the need for escape characters.   
let multiLineString = `This is a multi-line string.
It can span multiple lines without using escape characters.`;
console.log(multiLineString);


